def tr(key: str, **kwargs) -> str:
    """返回当前语言对应的字符串，支持 format 占位符。"""
    lang = ThemeManager.instance().language
    entry = _STRINGS.get(key)
    if entry is None:
        return key
    text = entry.get(lang, entry.get("zh", key))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, IndexError):
            pass
    return text
